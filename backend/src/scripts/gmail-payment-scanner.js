/**
 * Gmail Payment Scanner
 * 
 * Quét Gmail mỗi 30 giây để tìm email thông báo biến động số dư từ ngân hàng.
 * Khi phát hiện email chuyển khoản khớp với mã đơn hàng (transferContent),
 * script sẽ gọi webhook để kích hoạt VIP.
 * 
 * Cài đặt:
 * 1. Bật "Thông báo biến động số dư qua email" tại ngân hàng (BIDV, VCB, MB...)
 * 2. Bật IMAP cho Gmail: Gmail → Settings → See all settings → Forwarding and POP/IMAP → Enable IMAP
 * 3. Tạo App Password: Google Account → Security → 2-Step Verification → App passwords
 *    (Hoặc nếu chưa bật 2FA: cho phép "Less secure app access")
 * 4. Cấu hình .env:
 *    GMAIL_USER=your-email@gmail.com
 *    GMAIL_APP_PASSWORD=xxxx xxxx xxxx xxxx
 *    GMAIL_WEBHOOK_SECRET=your-strong-secret-here
 *    GMAIL_SCAN_INTERVAL=30000
 * 
 * Chạy:
 *   node src/scripts/gmail-payment-scanner.js
 * 
 * Hoặc chạy cùng PM2:
 *   pm2 start src/scripts/gmail-payment-scanner.js --name gmail-scanner
 */

const Imap = require("imap");
const { simpleParser } = require("mailparser");
const https = require("https");
const http = require("http");

// Load .env from backend root
require("dotenv").config({ path: require("path").resolve(__dirname, "../../.env") });

// ─── Configuration ─────────────────────────────────────────────
const CONFIG = {
  // Gmail IMAP credentials
  gmail: {
    user: process.env.GMAIL_USER,
    password: process.env.GMAIL_APP_PASSWORD,
    host: "imap.gmail.com",
    port: 993,
    tls: true,
    tlsOptions: { rejectUnauthorized: true },
  },

  // Webhook target (your backend server)
  webhook: {
    url: process.env.GMAIL_WEBHOOK_URL || "http://localhost:5000/hooks/gmail-payment",
    secret: process.env.GMAIL_WEBHOOK_SECRET || "",
  },

  // Scanning interval in ms (default 30s)
  scanInterval: parseInt(process.env.GMAIL_SCAN_INTERVAL, 10) || 30000,

  // Bank keywords to identify transaction emails
  // Add your bank's sender email or keywords here
  bankKeywords: (process.env.GMAIL_BANK_KEYWORDS || "BIDV,Vietcombank,VCB,MBBank,MB Bank,Techcombank,TCB,ACB,VPBank,TPBank,bien dong so du,biến động số dư,thong bao giao dich,thông báo giao dịch,bien lai chuyen tien,Biên lai chuyển tiền").split(",").map(k => k.trim().toLowerCase()),

  // Sender emails to filter (optional, for faster matching)
  // VCB real sender: VCBDigibank@info.vietcombank.com.vn (confirmed from actual emails)
  bankSenders: (process.env.GMAIL_BANK_SENDERS || "ebankinfo@bidv.com.vn,vcbdigibank@info.vietcombank.com.vn").split(",").map(s => s.trim().toLowerCase()).filter(Boolean),
};

// Validate required config
if (!CONFIG.gmail.user || !CONFIG.gmail.password) {
  console.error("❌ Missing GMAIL_USER or GMAIL_APP_PASSWORD in environment variables.");
  console.error("   Set them in .env file or environment.");
  process.exit(1);
}

if (!CONFIG.webhook.secret) {
  console.error("❌ Missing GMAIL_WEBHOOK_SECRET. Set a strong secret to secure the webhook.");
  process.exit(1);
}

// ─── State ─────────────────────────────────────────────────────
let lastScanTime = new Date();
let processedUIDs = new Set();
let isScanning = false;
let consecutiveErrors = 0;
const MAX_CONSECUTIVE_ERRORS = 10;

// ─── Logging ───────────────────────────────────────────────────
function log(level, message, data = null) {
  const timestamp = new Date().toISOString();
  const prefix = { info: "ℹ️", warn: "⚠️", error: "❌", success: "✅" }[level] || "📋";
  console.log(`[${timestamp}] ${prefix} ${message}`);
  if (data) console.log("   ", JSON.stringify(data, null, 2));
}

// ─── Parse bank email to extract transfer info ─────────────────
function extractTransferInfo(emailText) {
  if (!emailText) return null;

  // Normalize text: remove extra whitespace, decode HTML entities
  const text = emailText
    .replace(/<[^>]+>/g, " ")  // Strip HTML tags
    .replace(/&nbsp;/g, " ")
    .replace(/\s+/g, " ")
    .trim();

  // Pattern to find VIP transfer content code (format: VIP + 8 hex chars)
  // This matches the transferContent generated by createPayment: `VIP${shortId}`
  const transferContentMatch = text.match(/\b(VIP[0-9A-Fa-f]{8})\b/);
  if (!transferContentMatch) return null;

  const transferContent = transferContentMatch[1].toUpperCase();

  // Try to extract amount — covers major Vietnamese bank email formats:
  // VCB:   "Amount 22,000 VND"  (Số tiền / Amount on separate lines, tab-separated)
  // BIDV:  "+37,001 VND", "Số tiền ghi có: 37.001"
  // MB:    "Số tiền: 37.001đ", "GD: +37,001 VND"
  // TCB:   "PS ghi Có: +37,001 VND"
  // ACB:   "Amount: 37,001 VND"
  // TPBank: "Số tiền nhận: 37,001 VND"
  let amount = null;
  const amountPatterns = [
    // VCB format: "Amount 22,000 VND" (after tab→space normalization)
    /(?:Amount|Số tiền|So tien)[:\s]+([\d.,]+)\s*(?:VND|VNĐ|đ|dong)/i,
    // Generic: any number directly before VND/đ
    /\+?\s*([\d.,]+)\s*(?:VND|VNĐ|đ|dong)/i,
    /(?:GD|Giao dịch|Credit Amount)[:\s]+([\d.,]+)/i,
    /(?:PS|Phát sinh|Phat sinh)[:\s]+\+?([\d.,]+)/i,
    /(?:Số tiền ghi có|So tien ghi co|Ghi có|Ghi co|Credit)[:\s]+\+?([\d.,]+)/i,
    /(?:Số tiền nhận|So tien nhan|Tiền nhận|Tien nhan)[:\s]+\+?([\d.,]+)/i,
    /(?:PS ghi Có|PS ghi Co)[:\s]+\+?([\d.,]+)/i,
  ];

  for (const pattern of amountPatterns) {
    const match = text.match(pattern);
    if (match) {
      // Remove separators and parse
      amount = parseInt(match[1].replace(/[.,\s]/g, ""), 10);
      if (amount > 0) break;
    }
  }

  return { transferContent, amount, rawText: text.substring(0, 500) };
}

// ─── Check if email is from bank ───────────────────────────────
function isBankEmail(parsed) {
  const from = (parsed.from?.text || "").toLowerCase();
  const subject = (parsed.subject || "").toLowerCase();
  const text = (parsed.text || "").toLowerCase();

  // Check sender whitelist first (fastest)
  if (CONFIG.bankSenders.length > 0) {
    if (CONFIG.bankSenders.some(sender => from.includes(sender))) return true;
  }

  // Check keywords in subject or body
  return CONFIG.bankKeywords.some(keyword =>
    subject.includes(keyword) || from.includes(keyword) || text.includes(keyword)
  );
}

// ─── Call webhook to backend ───────────────────────────────────
function callWebhook(transferInfo) {
  return new Promise((resolve, reject) => {
    const payload = JSON.stringify({
      transferContent: transferInfo.transferContent,
      transferAmount: transferInfo.amount,
      source: "gmail-scanner",
      scannedAt: new Date().toISOString(),
    });

    const url = new URL(CONFIG.webhook.url);
    const isHttps = url.protocol === "https:";
    const transport = isHttps ? https : http;

    const options = {
      hostname: url.hostname,
      port: url.port || (isHttps ? 443 : 80),
      path: url.pathname,
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Content-Length": Buffer.byteLength(payload),
        "Authorization": `Bearer ${CONFIG.webhook.secret}`,
        "X-Scanner-Source": "gmail-payment-scanner",
      },
    };

    const req = transport.request(options, (res) => {
      let body = "";
      res.on("data", (chunk) => (body += chunk));
      res.on("end", () => {
        try {
          const parsed = JSON.parse(body);
          resolve({ status: res.statusCode, data: parsed });
        } catch {
          resolve({ status: res.statusCode, data: body });
        }
      });
    });

    req.on("error", reject);
    req.setTimeout(10000, () => {
      req.destroy(new Error("Webhook request timeout"));
    });
    req.write(payload);
    req.end();
  });
}

// ─── Scan Gmail for new bank emails ────────────────────────────
function scanGmail() {
  if (isScanning) {
    log("warn", "Previous scan still in progress, skipping...");
    return;
  }
  isScanning = true;

  const imap = new Imap(CONFIG.gmail);

  function handleError(err) {
    isScanning = false;
    consecutiveErrors++;
    log("error", `IMAP error (${consecutiveErrors}/${MAX_CONSECUTIVE_ERRORS}): ${err.message}`);

    if (consecutiveErrors >= MAX_CONSECUTIVE_ERRORS) {
      log("error", "Too many consecutive errors. Stopping scanner.");
      process.exit(1);
    }
  }

  imap.once("error", handleError);

  imap.once("ready", () => {
    imap.openBox("INBOX", true, (err) => {
      if (err) {
        imap.end();
        return handleError(err);
      }

      // Search for unseen emails from the last 2 minutes (overlap for safety)
      const searchSince = new Date(Date.now() - 2 * 60 * 1000);
      const sinceStr = searchSince.toISOString().split("T")[0]; // IMAP date format: YYYY-MM-DD

      // Search for UNSEEN emails since the date
      imap.search(["UNSEEN", ["SINCE", sinceStr]], (searchErr, uids) => {
        if (searchErr) {
          imap.end();
          return handleError(searchErr);
        }

        if (!uids || uids.length === 0) {
          consecutiveErrors = 0;
          isScanning = false;
          imap.end();
          return;
        }

        // Filter out already-processed UIDs
        const newUIDs = uids.filter(uid => !processedUIDs.has(uid));
        if (newUIDs.length === 0) {
          consecutiveErrors = 0;
          isScanning = false;
          imap.end();
          return;
        }

        log("info", `Found ${newUIDs.length} new email(s) to check`);

        const fetch = imap.fetch(newUIDs, { bodies: "", markSeen: false });
        const emailPromises = [];

        fetch.on("message", (msg, seqno) => {
          const emailPromise = new Promise((resolve) => {
            let uid = null;
            msg.on("attributes", (attrs) => { uid = attrs.uid; });

            msg.on("body", (stream) => {
              simpleParser(stream, async (parseErr, parsed) => {
                if (parseErr) {
                  log("warn", `Failed to parse email #${seqno}: ${parseErr.message}`);
                  resolve();
                  return;
                }

                // Mark as processed regardless
                if (uid) processedUIDs.add(uid);

                // Check if this is a bank transaction email
                if (!isBankEmail(parsed)) {
                  resolve();
                  return;
                }

                log("info", `Bank email detected: "${parsed.subject}" from ${parsed.from?.text}`);

                // Extract transfer info from email body
                const emailBody = parsed.text || parsed.html || "";
                const transferInfo = extractTransferInfo(emailBody);

                if (!transferInfo) {
                  log("warn", "Bank email found but no VIP transfer code detected");
                  resolve();
                  return;
                }

                log("info", `Transfer detected: code=${transferInfo.transferContent}, amount=${transferInfo.amount}`);

                // Call webhook
                try {
                  const result = await callWebhook(transferInfo);
                  log("success", `Webhook response: ${result.status}`, result.data);
                } catch (webhookErr) {
                  log("error", `Webhook call failed: ${webhookErr.message}`);
                }

                resolve();
              });
            });
          });

          emailPromises.push(emailPromise);
        });

        fetch.once("error", (fetchErr) => {
          log("error", `Fetch error: ${fetchErr.message}`);
        });

        fetch.once("end", async () => {
          await Promise.all(emailPromises);
          consecutiveErrors = 0;
          isScanning = false;
          lastScanTime = new Date();

          // Keep processedUIDs set from growing infinitely (keep last 1000)
          if (processedUIDs.size > 1000) {
            const arr = Array.from(processedUIDs);
            processedUIDs = new Set(arr.slice(arr.length - 500));
          }

          imap.end();
        });
      });
    });
  });

  imap.connect();
}

// ─── Main ──────────────────────────────────────────────────────
function main() {
  console.log("═══════════════════════════════════════════════");
  console.log("  📧 Gmail Payment Scanner");
  console.log("═══════════════════════════════════════════════");
  console.log(`  Gmail:     ${CONFIG.gmail.user}`);
  console.log(`  Webhook:   ${CONFIG.webhook.url}`);
  console.log(`  Interval:  ${CONFIG.scanInterval / 1000}s`);
  console.log(`  Keywords:  ${CONFIG.bankKeywords.slice(0, 5).join(", ")}...`);
  console.log("═══════════════════════════════════════════════\n");

  // Initial scan
  log("info", "Starting initial scan...");
  scanGmail();

  // Schedule periodic scans
  setInterval(scanGmail, CONFIG.scanInterval);

  // Graceful shutdown
  process.on("SIGINT", () => {
    log("info", "Shutting down scanner...");
    process.exit(0);
  });
  process.on("SIGTERM", () => {
    log("info", "Shutting down scanner...");
    process.exit(0);
  });
}

main();
